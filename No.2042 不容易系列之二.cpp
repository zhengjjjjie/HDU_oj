# include<iostream>
# include<vector> 
using namespace std;

int main() {
	// 动态规划 
	int n;
	while(cin >> n && n) {
		while(n--) {
			int m;
			cin >> m;
			vector<int> dp(m+1,0);
			dp[0] = 3;
			for(int i=1; i<=m; i++) {
				dp[i] = (dp[i-1]-1)*2;
			} 
			cout << dp[m] << endl;
		} 
	}
	return 0;
} 
